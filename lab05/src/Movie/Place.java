package src.Movie;

// class used to define a place to be used in the Movie class
public class Place {
    String country;
    String district;
    String city;
    String name;

    public Place(String coutry, String district, String city, String name){
        this.country = coutry;
        this.district = district;
        this.city = city;
        this.name = name;
    }
}
